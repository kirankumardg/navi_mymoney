package org.com.models;

public class MonthlyChange {
    String month;
    Double equityChange;
    Double debtChange;
    Double goldChange;

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public Double getEquityChange() {
        return equityChange;
    }

    public void setEquityChange(Double equityChange) {
        this.equityChange = equityChange;
    }

    public Double getDebtChange() {
        return debtChange;
    }

    public void setDebtChange(Double debtChange) {
        this.debtChange = debtChange;
    }

    public Double getGoldChange() {
        return goldChange;
    }

    public void setGoldChange(Double goldChange) {
        this.goldChange = goldChange;
    }



}
